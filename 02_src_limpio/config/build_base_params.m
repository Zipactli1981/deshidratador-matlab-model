function params_base = build_base_params()
%BUILD_BASE_PARAMS Common parameters for controlled three-mode GA runs.
    params_base.project.name = "GA_deshidratador_chile_red";
    params_base.project.version = "corrected_v2";
    params_base.project.product_label = "chile_red";
    params_base.product.name = "chile_red";
