    params_base.validation.allow_partial_campaign = false;
    params_base.validation.run_type = "formal";
    params_base.meta.created_by_function = "build_base_params";
    params_base.meta.created_at = datetime("now");
    assert(params_base.product.water_removed_target_kg > 0);
    assert(all(params_base.ga.lb < params_base.ga.ub));
end
