    params_base.product.mwf = Wf*params_base.product.m_f;
    params_base.product.water_removed_target_kg = params_base.product.mwi - params_base.product.mwf;
    params_base.ga.decision_vector = ["m_max","T_min","r_div2","t_rec_ini"];
