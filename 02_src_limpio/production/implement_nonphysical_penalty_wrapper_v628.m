function impl = implement_nonphysical_penalty_wrapper_v628()
% IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_v628
% Micropaso 6.28 — IMPLEMENT-NONPHYSICAL-PENALTY-WRAPPER-001
%
% Objetivo:
%   Implementar una version controlada del wrapper que invalide trayectorias
%   no fisicas antes de que produzcan resultados aparentemente favorables.
%
% Crea:
%   1) wrappers/opt_tunel_mod2_v16_nonphysical_penalty.m
%   2) production/objective_productive_corrected_v628_nonphysical_penalty.m
%
% Politica:
%   Si se detecta estado no fisico:
%       status = NONPHYSICAL_TRAJECTORY
%       MR_fin = 1000
%       M_prod_fin = NaN
%       Q_aux_tot = NaN
%       Irradiacion = NaN
%       dry_time = t(i)/3600 - t_ini
%
%   La objective clonada desde v611 debe convertir esa trayectoria en
%   penalizacion de optimizacion/postrun:
%       f = [1000, 1e6]
%
% No modifica:
%   - v10
%   - v611
%   - corrida productiva original
%
% Uso:
%   impl = implement_nonphysical_penalty_wrapper_v628();

    rootDir = setup_v05_paths();
    addpath(genpath(fullfile(rootDir,'02_src_limpio')));
    rehash;

    wrappersDir   = fullfile(rootDir,'02_src_limpio','wrappers');
    productionDir = fullfile(rootDir,'02_src_limpio','production');

    srcWrapper = fullfile(wrappersDir,'opt_tunel_mod2_v10_energy_mode_corrected.m');
    dstWrapper = fullfile(wrappersDir,'opt_tunel_mod2_v16_nonphysical_penalty.m');

    if ~isfile(srcWrapper)
        error('No existe wrapper fuente: %s', srcWrapper);
    end

    % ---------------------------------------------------------------------
    % Localizar corrida productiva
    % ---------------------------------------------------------------------
    baseDir = fullfile(rootDir,'05_runs','productive_v614b');

    d = dir(baseDir);
    d = d([d.isdir]);
    d = d(~ismember({d.name},{'.','..','.MATLABDriveTag'}));

    keep = false(size(d));
    for ii = 1:numel(d)
        keep(ii) = startsWith(d(ii).name,'PRODUCTIVE_GA_CORRECTED_v614_');
    end
    d = d(keep);

    if isempty(d)
        error('No se encontró corrida PRODUCTIVE_GA_CORRECTED_v614_* en %s', baseDir);
    end

    [~,idxRun] = max([d.datenum]);
    runDir = fullfile(baseDir,d(idxRun).name);

    tablesDir = fullfile(runDir,'tables');
    matDir    = fullfile(runDir,'mat');
    logsDir   = fullfile(runDir,'logs');
    traceDir  = fullfile(runDir,'trace_v628');

    if ~isfolder(traceDir), mkdir(traceDir); end
    if ~isfolder(tablesDir), mkdir(tablesDir); end
    if ~isfolder(matDir), mkdir(matDir); end
    if ~isfolder(logsDir), mkdir(logsDir); end

    selectedFile = fullfile(tablesDir,'SELECTED_SOLUTION_CORRECTED_v614b.csv');

    if ~isfile(selectedFile)
        error('No existe SELECTED_SOLUTION_CORRECTED_v614b.csv: %s', selectedFile);
    end

    Tsel = readtable(selectedFile);
    x = [Tsel.m_max(1), Tsel.T_min(1), Tsel.r_div2(1), Tsel.t_rec_ini(1)];

    % ---------------------------------------------------------------------
    % Parametros reales confirmados desde objective_productive_corrected_v611
    % ---------------------------------------------------------------------
    W0    = 200;
    m_i   = 0.8700;
    m_f   = 0.0800;

    Mi    = 6.6923;
    Mf    = 0.0870;
    M_des = 0.1111;

    md    = 26.0000;
    mwi   = 174.0000;
    mwf   = 2.2609;

    % ---------------------------------------------------------------------
    % Crear wrapper v16 desde v10
    % ---------------------------------------------------------------------
    txt = fileread(srcWrapper);

    oldSignature = ...
        'function [Q_aux_tot, dry_time, M_prod_fin, MR_fin, Irradiacion, irr_diag] = opt_tunel_mod2_v10_energy_mode_corrected(m_max,T_min,r_div2,t_rec_ini, W0, m_i, Mi, mwi, md, m_f, Mf, mwf, M_des, mode_operation)';

    newSignature = ...
        'function [Q_aux_tot, dry_time, M_prod_fin, MR_fin, Irradiacion, irr_diag] = opt_tunel_mod2_v16_nonphysical_penalty(m_max,T_min,r_div2,t_rec_ini, W0, m_i, Mi, mwi, md, m_f, Mf, mwf, M_des, mode_operation)';

    if ~contains(txt, oldSignature)
        error('No se encontró la firma esperada de v10.');
    end

    txt = strrep(txt, oldSignature, newSignature);

    globalBlock = sprintf([ ...
        '\n' ...
        '    global TRACE_V628_DIR TRACE_V628_MODE TRACE_V628_TAG\n' ...
        '\n' ...
    ]);

    txt = strrep(txt, newSignature, [newSignature, globalBlock]);

    % ---------------------------------------------------------------------
    % Insertar guarda fisica antes del bloque M_prod <= M_des
    % ---------------------------------------------------------------------
    oldDryBlockPattern = [
        '    if M_prod\(i\)<=M_des\s*' ...
        'Q_aux_tot=sum\(Q_aux\*t_step\*3600\)/1000;\s*' ...
        'Irradiacion=sum\(I\*t_step\*3600\*A_cap\*8\*mean\(ETHA_capt\)\)/1e6;\s*' ...
        'dry_time=t\(i\)/3600-t_ini;\s*' ...
        'M_prod_fin=M_prod\(i\);\s*' ...
        'MR_fin=MR\(i\);\s*' ...
        'break\s*' ...
        'end'
    ];

    guardBeforeDry = sprintf([ ...
        '    %% IMPLEMENT-NONPHYSICAL-PENALTY-WRAPPER-001\n' ...
        '    [nonphysical_v628, guard_diag_v628] = local_nonphysical_guard_v628( ...\n' ...
        '        i, Mf, ...\n' ...
        '        T_HE1_in, T_HE1_out, T_DH_in, T_DH_out, T_prod, T_amb, ...\n' ...
        '        HR_DH_in, HR_DH_out, HR_amb, ...\n' ...
        '        w_DH_in, w_DH_out, ...\n' ...
        '        M_prod, MR, Q_aux, I);\n' ...
        '\n' ...
        '    if nonphysical_v628\n' ...
        '        Q_aux_tot = NaN;\n' ...
        '        Irradiacion = NaN;\n' ...
        '        dry_time = t(i)/3600-t_ini;\n' ...
        '        M_prod_fin = NaN;\n' ...
        '        MR_fin = 1000;\n' ...
        '\n' ...
        '        irr_diag.status = "NONPHYSICAL_TRAJECTORY";\n' ...
        '        irr_diag.termination_status = "NONPHYSICAL_TRAJECTORY";\n' ...
        '        irr_diag.nonphysical_guard = guard_diag_v628;\n' ...
        '        irr_diag.mode_operation = string(mode_operation);\n' ...
        '\n' ...
        '        termination_status_v628 = "NONPHYSICAL_TRAJECTORY";\n' ...
        '        break_i_v628 = i;\n' ...
        '        guard_diag_saved_v628 = guard_diag_v628;\n' ...
        '\n' ...
        '        if ~isempty(TRACE_V628_DIR)\n' ...
        '            if ~isfolder(TRACE_V628_DIR), mkdir(TRACE_V628_DIR); end\n' ...
        '            safeMode_v628 = char(string(TRACE_V628_MODE));\n' ...
        '            safeMode_v628 = regexprep(safeMode_v628,''[^a-zA-Z0-9_]'',''_'');\n' ...
        '            safeTag_v628 = char(string(TRACE_V628_TAG));\n' ...
        '            safeTag_v628 = regexprep(safeTag_v628,''[^a-zA-Z0-9_]'',''_'');\n' ...
        '            trace_file_v628 = fullfile(TRACE_V628_DIR, sprintf(''TRACE_v628_%%s_%%s_nonphysical.mat'', safeMode_v628, safeTag_v628));\n' ...
        '            save(trace_file_v628);\n' ...
        '        end\n' ...
        '\n' ...
        '        break\n' ...
        '    end\n\n' ...
    ]);

    [s1,e1] = regexp(txt, oldDryBlockPattern, 'start', 'end');

    if numel(s1) ~= 1
        error('No se encontró exactamente una vez el bloque M_prod<=M_des. Coincidencias: %d', numel(s1));
    end

    oldDryBlockText = txt(s1:e1);
    txt = [txt(1:s1-1), guardBeforeDry, oldDryBlockText, txt(e1+1:end)];

    % ---------------------------------------------------------------------
    % Instrumentar salida normal por M_prod <= M_des para traza
    % ---------------------------------------------------------------------
    oldDryBlockPattern2 = [
        '    if M_prod\(i\)<=M_des\s*' ...
        'Q_aux_tot=sum\(Q_aux\*t_step\*3600\)/1000;\s*' ...
        'Irradiacion=sum\(I\*t_step\*3600\*A_cap\*8\*mean\(ETHA_capt\)\)/1e6;\s*' ...
        'dry_time=t\(i\)/3600-t_ini;\s*' ...
        'M_prod_fin=M_prod\(i\);\s*' ...
        'MR_fin=MR\(i\);\s*' ...
        'break\s*' ...
        'end'
    ];

    newDryBlock = sprintf([ ...
        '    if M_prod(i)<=M_des\n' ...
        '        Q_aux_tot=sum(Q_aux*t_step*3600)/1000;\n' ...
        '        Irradiacion=sum(I*t_step*3600*A_cap*8*mean(ETHA_capt))/1e6;\n' ...
        '        dry_time=t(i)/3600-t_ini;\n' ...
        '        M_prod_fin=M_prod(i);\n' ...
        '        MR_fin=MR(i);\n' ...
        '\n' ...
        '        irr_diag.status = "OK";\n' ...
        '        irr_diag.termination_status = "M_DES_REACHED";\n' ...
        '        irr_diag.mode_operation = string(mode_operation);\n' ...
        '\n' ...
        '        termination_status_v628 = "M_DES_REACHED";\n' ...
        '        break_i_v628 = i;\n' ...
        '        guard_diag_saved_v628 = struct();\n' ...
        '        guard_diag_saved_v628.status = "OK";\n' ...
        '\n' ...
        '        if ~isempty(TRACE_V628_DIR)\n' ...
        '            if ~isfolder(TRACE_V628_DIR), mkdir(TRACE_V628_DIR); end\n' ...
        '            safeMode_v628 = char(string(TRACE_V628_MODE));\n' ...
        '            safeMode_v628 = regexprep(safeMode_v628,''[^a-zA-Z0-9_]'',''_'');\n' ...
        '            safeTag_v628 = char(string(TRACE_V628_TAG));\n' ...
        '            safeTag_v628 = regexprep(safeTag_v628,''[^a-zA-Z0-9_]'',''_'');\n' ...
        '            trace_file_v628 = fullfile(TRACE_V628_DIR, sprintf(''TRACE_v628_%%s_%%s_normal.mat'', safeMode_v628, safeTag_v628));\n' ...
        '            save(trace_file_v628);\n' ...
        '        end\n' ...
        '\n' ...
        '        break\n' ...
        '    end' ...
    ]);

    [s2,e2] = regexp(txt, oldDryBlockPattern2, 'start', 'end');

    if numel(s2) ~= 1
        error('No se encontró exactamente una vez el bloque M_prod<=M_des despues de insertar guarda. Coincidencias: %d', numel(s2));
    end

    txt = [txt(1:s2-1), newDryBlock, txt(e2+1:end)];

    % ---------------------------------------------------------------------
    % Instrumentar salida por t_max para traza y conservar logica original
    % ---------------------------------------------------------------------
    oldTmaxBlockPattern = [
        '    if t\(i\)>=\(t_max-t_step\)\*3600\s*' ...
        'Q_aux_tot=sum\(Q_aux\*t_step\*3600\)/1000;\s*' ...
        'Irradiacion=sum\(I\*t_step\*3600\*A_cap\*8\*mean\(ETHA_capt\)\)/1e6;\s*' ...
        'dry_time=t\(i\)/3600-t_ini;\s*' ...
        'M_prod_fin=min\(M_prod\);\s*' ...
        'MR_fin=MR\(end-1\);\s*' ...
        'break\s*' ...
        'end'
    ];

    newTmaxBlock = sprintf([ ...
        '    if t(i)>=(t_max-t_step)*3600\n' ...
        '        Q_aux_tot=sum(Q_aux*t_step*3600)/1000;\n' ...
        '        Irradiacion=sum(I*t_step*3600*A_cap*8*mean(ETHA_capt))/1e6;\n' ...
        '        dry_time=t(i)/3600-t_ini;\n' ...
        '        M_prod_fin=min(M_prod);\n' ...
        '        MR_fin=MR(end-1);\n' ...
        '\n' ...
        '        irr_diag.status = "OK";\n' ...
        '        irr_diag.termination_status = "TMAX_REACHED";\n' ...
        '        irr_diag.mode_operation = string(mode_operation);\n' ...
        '\n' ...
        '        termination_status_v628 = "TMAX_REACHED";\n' ...
        '        break_i_v628 = i;\n' ...
        '        guard_diag_saved_v628 = struct();\n' ...
        '        guard_diag_saved_v628.status = "OK";\n' ...
        '\n' ...
        '        if ~isempty(TRACE_V628_DIR)\n' ...
        '            if ~isfolder(TRACE_V628_DIR), mkdir(TRACE_V628_DIR); end\n' ...
        '            safeMode_v628 = char(string(TRACE_V628_MODE));\n' ...
        '            safeMode_v628 = regexprep(safeMode_v628,''[^a-zA-Z0-9_]'',''_'');\n' ...
        '            safeTag_v628 = char(string(TRACE_V628_TAG));\n' ...
        '            safeTag_v628 = regexprep(safeTag_v628,''[^a-zA-Z0-9_]'',''_'');\n' ...
        '            trace_file_v628 = fullfile(TRACE_V628_DIR, sprintf(''TRACE_v628_%%s_%%s_tmax.mat'', safeMode_v628, safeTag_v628));\n' ...
        '            save(trace_file_v628);\n' ...
        '        end\n' ...
        '\n' ...
        '        break\n' ...
        '    end' ...
    ]);

    [s3,e3] = regexp(txt, oldTmaxBlockPattern, 'start', 'end');

    if numel(s3) ~= 1
        error('No se encontró exactamente una vez el bloque t_max. Coincidencias: %d', numel(s3));
    end

    txt = [txt(1:s3-1), newTmaxBlock, txt(e3+1:end)];

    % ---------------------------------------------------------------------
    % Agregar helper local al final del wrapper
    % ---------------------------------------------------------------------
    helperText = sprintf([ ...
        '\n\n' ...
        'function [bad, diag] = local_nonphysical_guard_v628(i, Mf, ...\n' ...
        '    T_HE1_in, T_HE1_out, T_DH_in, T_DH_out, T_prod, T_amb, ...\n' ...
        '    HR_DH_in, HR_DH_out, HR_amb, ...\n' ...
        '    w_DH_in, w_DH_out, ...\n' ...
        '    M_prod, MR, Q_aux, I)\n' ...
        '%% LOCAL_NONPHYSICAL_GUARD_v628\n' ...
        '%% Devuelve bad=true si el estado actual i viola dominio fisico.\n' ...
        '\n' ...
        '    bad = false;\n' ...
        '\n' ...
        '    diag = struct();\n' ...
        '    diag.status = "OK";\n' ...
        '    diag.first_i = NaN;\n' ...
        '    diag.variable = "";\n' ...
        '    diag.rule = "";\n' ...
        '    diag.value_real = NaN;\n' ...
        '    diag.value_imag = NaN;\n' ...
        '    diag.Mf = Mf;\n' ...
        '\n' ...
        '    checks = {};\n' ...
        '\n' ...
        '    checks{end+1} = local_make_check_v628("T_HE1_in",  T_HE1_in,  "temperature_low_high", -10, 120, 10);\n' ...
        '    checks{end+1} = local_make_check_v628("T_HE1_out", T_HE1_out, "temperature_low_high", -10, 120, 10);\n' ...
        '    checks{end+1} = local_make_check_v628("T_DH_in",   T_DH_in,   "temperature_low_high", -10, 120, 10);\n' ...
        '    checks{end+1} = local_make_check_v628("T_DH_out",  T_DH_out,  "temperature_low_high", -10, 120, 10);\n' ...
        '    checks{end+1} = local_make_check_v628("T_prod",    T_prod,    "temperature_low_high", -10, 120, 10);\n' ...
        '    checks{end+1} = local_make_check_v628("T_amb",     T_amb,     "temperature_low_high", -10, 120, 10);\n' ...
        '\n' ...
        '    checks{end+1} = local_make_check_v628("HR_DH_in",  HR_DH_in,  "relative_humidity_0_1", 0, 1, 20);\n' ...
        '    checks{end+1} = local_make_check_v628("HR_DH_out", HR_DH_out, "relative_humidity_0_1", 0, 1, 20);\n' ...
        '    checks{end+1} = local_make_check_v628("HR_amb",    HR_amb,    "relative_humidity_0_1", 0, 1, 20);\n' ...
        '\n' ...
        '    checks{end+1} = local_make_check_v628("w_DH_in",   w_DH_in,   "humidity_ratio_nonnegative", 0, Inf, 30);\n' ...
        '    checks{end+1} = local_make_check_v628("w_DH_out",  w_DH_out,  "humidity_ratio_nonnegative", 0, Inf, 30);\n' ...
        '\n' ...
        '    checks{end+1} = local_make_check_v628("M_prod",    M_prod,    "moisture_content_ge_Mf", Mf, Inf, 40);\n' ...
        '    checks{end+1} = local_make_check_v628("MR",        MR,        "MR_0_1", 0, 1, 50);\n' ...
        '    checks{end+1} = local_make_check_v628("Q_aux",     Q_aux,     "energy_aux_nonnegative", 0, Inf, 60);\n' ...
        '    checks{end+1} = local_make_check_v628("I",         I,         "irradiance_nonnegative", 0, Inf, 60);\n' ...
        '\n' ...
        '    found = {};\n' ...
        '\n' ...
        '    for cc = 1:numel(checks)\n' ...
        '        chk = checks{cc};\n' ...
        '        [isbad, one] = local_eval_check_v628(i, chk);\n' ...
        '        if isbad\n' ...
        '            found{end+1,1} = one; %#ok<AGROW>\n' ...
        '        end\n' ...
        '    end\n' ...
        '\n' ...
        '    if isempty(found)\n' ...
        '        return\n' ...
        '    end\n' ...
        '\n' ...
        '    bad = true;\n' ...
        '    D = struct2table(vertcat(found{:}));\n' ...
        '    D = sortrows(D,{''priority'',''variable''});\n' ...
        '\n' ...
        '    diag.status = "NONPHYSICAL_TRAJECTORY";\n' ...
        '    diag.first_i = i;\n' ...
        '    diag.variable = string(D.variable(1));\n' ...
        '    diag.rule = string(D.rule(1));\n' ...
        '    diag.value_real = D.value_real(1);\n' ...
        '    diag.value_imag = D.value_imag(1);\n' ...
        '    diag.priority = D.priority(1);\n' ...
        '    diag.all_violations = D;\n' ...
        'end\n' ...
        '\n' ...
        'function chk = local_make_check_v628(name, vec, rule, lower, upper, priority)\n' ...
        '    chk = struct();\n' ...
        '    chk.name = string(name);\n' ...
        '    chk.vec = vec;\n' ...
        '    chk.rule = string(rule);\n' ...
        '    chk.lower = lower;\n' ...
        '    chk.upper = upper;\n' ...
        '    chk.priority = priority;\n' ...
        'end\n' ...
        '\n' ...
        'function [isbad, one] = local_eval_check_v628(i, chk)\n' ...
        '    isbad = false;\n' ...
        '    one = struct();\n' ...
        '\n' ...
        '    v = chk.vec;\n' ...
        '    if isempty(v)\n' ...
        '        return\n' ...
        '    end\n' ...
        '\n' ...
        '    idx = min(max(1,i),numel(v));\n' ...
        '    val = double(v(idx));\n' ...
        '    rv = real(val);\n' ...
        '    iv = imag(val);\n' ...
        '\n' ...
        '    bad_naninf = isnan(rv) || isinf(rv) || isnan(iv) || isinf(iv);\n' ...
        '    bad_complex = abs(iv) > 1e-9;\n' ...
        '\n' ...
        '    switch char(chk.rule)\n' ...
        '        case ''temperature_low_high''\n' ...
        '            bad_rule = rv < chk.lower || rv > chk.upper;\n' ...
        '        case ''relative_humidity_0_1''\n' ...
        '            bad_rule = rv < chk.lower || rv > chk.upper;\n' ...
        '        case ''humidity_ratio_nonnegative''\n' ...
        '            bad_rule = rv < chk.lower;\n' ...
        '        case ''moisture_content_ge_Mf''\n' ...
        '            bad_rule = rv < (chk.lower - 1e-8);\n' ...
        '        case ''MR_0_1''\n' ...
        '            bad_rule = rv < chk.lower || rv > chk.upper;\n' ...
        '        case ''energy_aux_nonnegative''\n' ...
        '            bad_rule = rv < chk.lower;\n' ...
        '        case ''irradiance_nonnegative''\n' ...
        '            bad_rule = rv < chk.lower;\n' ...
        '        otherwise\n' ...
        '            bad_rule = false;\n' ...
        '    end\n' ...
        '\n' ...
        '    if ~(bad_naninf || bad_complex || bad_rule)\n' ...
        '        return\n' ...
        '    end\n' ...
        '\n' ...
        '    isbad = true;\n' ...
        '\n' ...
        '    if bad_naninf\n' ...
        '        finalRule = chk.rule + "_nan_or_inf";\n' ...
        '        priority = chk.priority - 2;\n' ...
        '    elseif bad_complex\n' ...
        '        finalRule = chk.rule + "_complex";\n' ...
        '        priority = chk.priority - 1;\n' ...
        '    else\n' ...
        '        finalRule = chk.rule;\n' ...
        '        priority = chk.priority;\n' ...
        '    end\n' ...
        '\n' ...
        '    one.variable = string(chk.name);\n' ...
        '    one.rule = string(finalRule);\n' ...
        '    one.i = i;\n' ...
        '    one.value_real = rv;\n' ...
        '    one.value_imag = iv;\n' ...
        '    one.priority = priority;\n' ...
        'end\n' ...
    ]);

    txt = [txt, helperText];

    fid = fopen(dstWrapper,'w');
    if fid < 0
        error('No se pudo crear wrapper v16: %s', dstWrapper);
    end
    fwrite(fid,txt);
    fclose(fid);

    clear opt_tunel_mod2_v16_nonphysical_penalty
    rehash;

    if isempty(which('opt_tunel_mod2_v16_nonphysical_penalty'))
        error('Se creó v16, pero MATLAB no lo encuentra en path.');
    end

    % ---------------------------------------------------------------------
    % Crear objective v628 clonada desde v611
    % ---------------------------------------------------------------------
    obj611 = which('objective_productive_corrected_v611');

    if isempty(obj611)
        error('No se encontró objective_productive_corrected_v611.');
    end

    obj628 = fullfile(productionDir,'objective_productive_corrected_v628_nonphysical_penalty.m');

    objTxt = fileread(obj611);

    objTxt = strrep(objTxt, ...
        'objective_productive_corrected_v611', ...
        'objective_productive_corrected_v628_nonphysical_penalty');

    objTxt = strrep(objTxt, ...
        'opt_tunel_mod2_v10_energy_mode_corrected', ...
        'opt_tunel_mod2_v16_nonphysical_penalty');

    fid = fopen(obj628,'w');
    if fid < 0
        error('No se pudo crear objective v628: %s', obj628);
    end
    fwrite(fid,objTxt);
    fclose(fid);

    clear objective_productive_corrected_v628_nonphysical_penalty
    rehash;

    if isempty(which('objective_productive_corrected_v628_nonphysical_penalty'))
        error('Se creó objective v628, pero MATLAB no la encuentra en path.');
    end

    % ---------------------------------------------------------------------
    % Evaluacion directa wrapper v16 y objective v628
    % ---------------------------------------------------------------------
    global TRACE_V628_DIR TRACE_V628_MODE TRACE_V628_TAG

    modes = ["gasLP","hybrid","solar"];

    directRows = {};
    objRows = {};

    for k = 1:numel(modes)
        mode = modes(k);

        TRACE_V628_DIR = traceDir;
        TRACE_V628_MODE = mode;
        TRACE_V628_TAG = "selected_solution";

        filesToDelete = dir(fullfile(traceDir, sprintf('TRACE_v628_%s_selected_solution_*.mat', char(mode))));
        for ff = 1:numel(filesToDelete)
            delete(fullfile(traceDir, filesToDelete(ff).name));
        end

        % Direct wrapper
        row = struct();
        row.mode = mode;

        try
            [Q_aux_tot, dry_time, M, MR, Irradiacion, irr_diag] = ...
                opt_tunel_mod2_v16_nonphysical_penalty( ...
                    x(1), x(2), x(3), x(4), ...
                    W0, m_i, Mi, mwi, md, m_f, Mf, mwf, M_des, mode);

            row.status = "OK";
            row.error_message = "";
            row.Q_aux_tot = Q_aux_tot;
            row.Irradiacion = Irradiacion;
            row.dry_time = dry_time;
            row.M = M;
            row.MR = MR;

            if exist('irr_diag','var') && isstruct(irr_diag) && isfield(irr_diag,'status')
                row.irr_status = string(irr_diag.status);
            else
                row.irr_status = "UNKNOWN";
            end

            if exist('irr_diag','var') && isstruct(irr_diag) && isfield(irr_diag,'termination_status')
                row.termination_status = string(irr_diag.termination_status);
            else
                row.termination_status = "UNKNOWN";
            end

            if exist('irr_diag','var') && isstruct(irr_diag) && isfield(irr_diag,'nonphysical_guard')
                gd = irr_diag.nonphysical_guard;
                row.guard_variable = string(gd.variable);
                row.guard_rule = string(gd.rule);
                row.guard_i = gd.first_i;
                row.guard_value_real = gd.value_real;
                row.guard_value_imag = gd.value_imag;
            else
                row.guard_variable = "";
                row.guard_rule = "";
                row.guard_i = NaN;
                row.guard_value_real = NaN;
                row.guard_value_imag = NaN;
            end

        catch ME
            row.status = "ERROR";
            row.error_message = string(ME.message);
            row.Q_aux_tot = NaN;
            row.Irradiacion = NaN;
            row.dry_time = NaN;
            row.M = NaN;
            row.MR = NaN;
            row.irr_status = "ERROR";
            row.termination_status = "ERROR";
            row.guard_variable = "";
            row.guard_rule = "";
            row.guard_i = NaN;
            row.guard_value_real = NaN;
            row.guard_value_imag = NaN;
        end

        nonphysFile = fullfile(traceDir, sprintf('TRACE_v628_%s_selected_solution_nonphysical.mat', char(mode)));
        normalFile  = fullfile(traceDir, sprintf('TRACE_v628_%s_selected_solution_normal.mat', char(mode)));
        tmaxFile    = fullfile(traceDir, sprintf('TRACE_v628_%s_selected_solution_tmax.mat', char(mode)));

        row.trace_nonphysical_exists = isfile(nonphysFile);
        row.trace_normal_exists = isfile(normalFile);
        row.trace_tmax_exists = isfile(tmaxFile);
        row.trace_nonphysical_file = string(nonphysFile);
        row.trace_normal_file = string(normalFile);
        row.trace_tmax_file = string(tmaxFile);

        directRows{end+1,1} = row; %#ok<AGROW>

        % Objective v628
        orow = struct();
        orow.mode = mode;

        try
            [f628,d628] = objective_productive_corrected_v628_nonphysical_penalty(x, mode);

            orow.status = "OK";
            orow.error_message = "";
            orow.MR_objective = f628(1);
            orow.cost_objective = f628(2);

            if isfield(d628,'status')
                orow.detail_status = string(d628.status);
            else
                orow.detail_status = "UNKNOWN";
            end

            if isfield(d628,'outputs') && isfield(d628.outputs,'M')
                orow.M = d628.outputs.M;
            else
                orow.M = NaN;
            end

            if isfield(d628,'outputs') && isfield(d628.outputs,'MR')
                orow.MR = d628.outputs.MR;
            else
                orow.MR = NaN;
            end

            if isfield(d628,'outputs') && isfield(d628.outputs,'Q_aux_tot')
                orow.Q_aux_tot = d628.outputs.Q_aux_tot;
            else
                orow.Q_aux_tot = NaN;
            end

            if isfield(d628,'outputs') && isfield(d628.outputs,'Irradiacion')
                orow.Irradiacion = d628.outputs.Irradiacion;
            else
                orow.Irradiacion = NaN;
            end

        catch ME
            orow.status = "ERROR";
            orow.error_message = string(ME.message);
            orow.MR_objective = NaN;
            orow.cost_objective = NaN;
            orow.detail_status = "ERROR";
            orow.M = NaN;
            orow.MR = NaN;
            orow.Q_aux_tot = NaN;
            orow.Irradiacion = NaN;
        end

        objRows{end+1,1} = orow; %#ok<AGROW>
    end

    Tdirect = struct2table(vertcat(directRows{:}));
    Tobj = struct2table(vertcat(objRows{:}));

    % ---------------------------------------------------------------------
    % Diagnostico
    % ---------------------------------------------------------------------
    flags = struct();

    gasRowD = strcmp(Tdirect.mode,"gasLP");
    hybRowD = strcmp(Tdirect.mode,"hybrid");
    solRowD = strcmp(Tdirect.mode,"solar");

    gasRowO = strcmp(Tobj.mode,"gasLP");
    hybRowO = strcmp(Tobj.mode,"hybrid");
    solRowO = strcmp(Tobj.mode,"solar");

    flags.direct_all_status_ok = all(strcmp(Tdirect.status,"OK"));
    flags.objective_all_status_ok = all(strcmp(Tobj.status,"OK"));

    flags.gas_direct_ok = strcmp(Tdirect.irr_status(gasRowD),"OK");
    flags.hybrid_direct_ok = strcmp(Tdirect.irr_status(hybRowD),"OK");
    flags.solar_direct_nonphysical = strcmp(Tdirect.irr_status(solRowD),"NONPHYSICAL_TRAJECTORY");

    flags.solar_trace_nonphysical_exists = Tdirect.trace_nonphysical_exists(solRowD);

    flags.solar_objective_penalized = ...
        Tobj.MR_objective(solRowO) >= 1000 || Tobj.cost_objective(solRowO) >= 1e6;

    flags.gas_objective_not_penalized = ...
        Tobj.MR_objective(gasRowO) < 1000 && Tobj.cost_objective(gasRowO) < 1e6;

    flags.hybrid_objective_not_penalized = ...
        Tobj.MR_objective(hybRowO) < 1000 && Tobj.cost_objective(hybRowO) < 1e6;

    if ~flags.direct_all_status_ok
        diagnosis = "IMPLEMENTATION_DIRECT_WRAPPER_ERROR";
    elseif ~flags.objective_all_status_ok
        diagnosis = "IMPLEMENTATION_OBJECTIVE_ERROR";
    elseif flags.solar_direct_nonphysical && flags.gas_direct_ok && flags.hybrid_direct_ok && ...
            flags.solar_objective_penalized && flags.gas_objective_not_penalized && flags.hybrid_objective_not_penalized
        diagnosis = "NONPHYSICAL_PENALTY_IMPLEMENTATION_PASS";
    elseif flags.solar_direct_nonphysical && flags.gas_direct_ok && flags.hybrid_direct_ok
        diagnosis = "WRAPPER_PENALTY_PASS_OBJECTIVE_POLICY_REVIEW";
    else
        diagnosis = "NONPHYSICAL_PENALTY_IMPLEMENTATION_REQUIRES_REVIEW";
    end

    % ---------------------------------------------------------------------
    % Guardar salidas
    % ---------------------------------------------------------------------
    outCsvDirect = fullfile(tablesDir,'IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_v628_direct.csv');
    outCsvObj    = fullfile(tablesDir,'IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_v628_objective.csv');
    outMat       = fullfile(matDir,'IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_v628.mat');
    outLog       = fullfile(logsDir,'IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_v628.txt');

    writetable(Tdirect,outCsvDirect);
    writetable(Tobj,outCsvObj);

    save(outMat,'Tdirect','Tobj','flags','diagnosis','x','runDir','traceDir','dstWrapper','obj628');

    fid = fopen(outLog,'w');
    fprintf(fid,'IMPLEMENT-NONPHYSICAL-PENALTY-WRAPPER-001\n');
    fprintf(fid,'status: IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_COMPLETED\n');
    fprintf(fid,'diagnosis: %s\n', diagnosis);
    fprintf(fid,'runDir: %s\n', runDir);
    fprintf(fid,'traceDir: %s\n', traceDir);
    fprintf(fid,'dstWrapper: %s\n', dstWrapper);
    fprintf(fid,'obj628: %s\n', obj628);
    fprintf(fid,'outCsvDirect: %s\n', outCsvDirect);
    fprintf(fid,'outCsvObj: %s\n', outCsvObj);
    fprintf(fid,'outMat: %s\n', outMat);
    fprintf(fid,'timestamp: %s\n\n', datestr(now,'yyyy-mm-dd HH:MM:SS'));

    fprintf(fid,'--- DIRECT WRAPPER TABLE ---\n');
    for r = 1:height(Tdirect)
        fprintf(fid,'mode: %s\n', Tdirect.mode(r));
        fprintf(fid,'status: %s\n', Tdirect.status(r));
        fprintf(fid,'irr_status: %s\n', Tdirect.irr_status(r));
        fprintf(fid,'termination_status: %s\n', Tdirect.termination_status(r));
        fprintf(fid,'Q_aux_tot: %.12g\n', Tdirect.Q_aux_tot(r));
        fprintf(fid,'Irradiacion: %.12g\n', Tdirect.Irradiacion(r));
        fprintf(fid,'dry_time: %.12g\n', Tdirect.dry_time(r));
        fprintf(fid,'M: %.12g\n', Tdirect.M(r));
        fprintf(fid,'MR: %.12g\n', Tdirect.MR(r));
        fprintf(fid,'guard_variable: %s\n', Tdirect.guard_variable(r));
        fprintf(fid,'guard_rule: %s\n', Tdirect.guard_rule(r));
        fprintf(fid,'guard_i: %.0f\n', Tdirect.guard_i(r));
        fprintf(fid,'guard_value_real: %.12g\n\n', Tdirect.guard_value_real(r));
    end

    fprintf(fid,'--- OBJECTIVE TABLE ---\n');
    for r = 1:height(Tobj)
        fprintf(fid,'mode: %s\n', Tobj.mode(r));
        fprintf(fid,'status: %s\n', Tobj.status(r));
        fprintf(fid,'MR_objective: %.12g\n', Tobj.MR_objective(r));
        fprintf(fid,'cost_objective: %.12g\n', Tobj.cost_objective(r));
        fprintf(fid,'detail_status: %s\n', Tobj.detail_status(r));
        fprintf(fid,'Q_aux_tot: %.12g\n', Tobj.Q_aux_tot(r));
        fprintf(fid,'Irradiacion: %.12g\n', Tobj.Irradiacion(r));
        fprintf(fid,'M: %.12g\n', Tobj.M(r));
        fprintf(fid,'MR: %.12g\n\n', Tobj.MR(r));
    end

    fclose(fid);

    impl = struct();
    impl.status = 'IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_COMPLETED';
    impl.diagnosis = diagnosis;
    impl.flags = flags;
    impl.Tdirect = Tdirect;
    impl.Tobj = Tobj;
    impl.runDir = runDir;
    impl.traceDir = traceDir;
    impl.dstWrapper = dstWrapper;
    impl.obj628 = obj628;
    impl.outCsvDirect = outCsvDirect;
    impl.outCsvObj = outCsvObj;
    impl.outMat = outMat;
    impl.outLog = outLog;

    disp('=== IMPLEMENT_NONPHYSICAL_PENALTY_WRAPPER_v628 ===')
    disp(impl.status)
    disp('=== DIAGNOSIS ===')
    disp(impl.diagnosis)
    disp('=== FLAGS ===')
    disp(impl.flags)
    disp('=== DIRECT WRAPPER TABLE ===')
    disp(Tdirect)
    disp('=== OBJECTIVE TABLE ===')
    disp(Tobj)
    disp('=== OUTPUT FILES ===')
    disp(impl.outCsvDirect)
    disp(impl.outCsvObj)
    disp(impl.outMat)
    disp(impl.outLog)
end